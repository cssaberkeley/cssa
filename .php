<img src="images/conference.png" align="left" style="padding-right:40px; padding-top:20px; " /><h2>California Cognitive Science Conference</h2>

<p>The annual California Cognitive Science Conference is the highlight of each year for the CSSA.  The conference in late Spring brings together students, faculty, and researchers from across the state and around the world. It features talks from renowned Cognitive Science researchers regarding their areas of expertise.  Additionally, attendees explore the various fields of Cognitive Science via workshops, demonstrations and panel discussions.</p><p>

 Lastly, our annual conference features a poster session to give undergraduates from every university a chance to present their research findings to an eager, receptive audience.  Attendees enjoy a day to immerse themselves in the latest discoveries in this rapidly growing field, associate with fellow Cognitive Scientists, and most importantly, have fun!</p>
 
 


<h3 align="center">2012 California Cognitive Science Conference <font color="#0033CC"><i></h3>
<p align="center">This year's conference will be held on <strong>April 28, 2012</strong> in UC Berkeley's <strong>Stanley Hall</strong> (<a href="http://berkeley.edu/map/maps/BC56.html">http://berkeley.edu/map/maps/BC56.html</a>). The title for this year's conference will be <strong>&quot;Consciousness: Nature of Mind&quot;</strong>.</i></font></p>
